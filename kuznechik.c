static uint8_t kuz_mul_gf256( uint8_t x, uint8_t y )
{
	uint8_t z;
	
	z = 0;
	while (y) 
	{		
		if ( y & 1 ) z ^= x;
		x = (x << 1) ^ (x & 0x80 ? 0xC3 : 0x00);
		y >>= 1;
	}
		
	return z;
}

