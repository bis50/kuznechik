// Линейное преобразование.
static void l_Transformation( uint8_t vector[16] )
{
	int i, n;
	uint8_t x;
	
	for(n=0; n<16; n++) 
	{
		x = vector[15];
		for(i=14; i>=0; i--) 
		{
			vector[i+1] = vector[i];
			x ^= kuz_mul_gf256( vector[i], lvec[i] );
		}
		vector[0] = x;
	}
}

// Обратное линейное преобразование.
static void l_Transformation_Inv( uint8_t vector[16] )
{
	int i, n;
	uint8_t x;
	
	for(n=0; n<16; n++) 
	{

		x = vector[0];
		for(i=0; i<15; i++) 
		{
			vector[i] = vector[i+1];	
			x ^= kuz_mul_gf256( vector[i], lvec[i] );
		}
		vector[15] = x;
	}
}

