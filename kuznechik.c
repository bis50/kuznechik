// Задание секретного ключа.                                          
void kuz_set_key( const uint8_t key[32], kuz_context* cntx )
{              
int round;
int i;  
int n;       
static uint8_t k_buf[16];
const uint8_t * p_c_X = c_X;
uint8_t * R;
uint8_t * L;
uint8_t * swp;

	assert( cntx != NULL );

	/*
		На основе заданного секретного ключа вычисляются итерационные ключи,
		используемые для операций шифрования/дешифрования.
		Итерационых ключей 10 длиной 16 байт каждый. 
		
		Для формирования ключей используется преобразование по схеме ячейки Фейстеля.
	*/

	memcpy( cntx->round_key_X[0], key,    16*sizeof(uint8_t) );
	memcpy( cntx->round_key_X[1], key+16, 16*sizeof(uint8_t) );

	assert( 5 == (sizeof(cntx->round_key_X)/sizeof(cntx->round_key_X[0])/2) ); // 5 пар ключей.

	for(round=1; round<5; round++)
	{
		// Оптимизация...
		// round_key_X[(round+1)*2] <= round_key_X[round*2]
		// round_key_X[(round+1)*2+1] <= round_key_X[round*2+1]

		L = cntx->round_key_X[2*round]; R = L+16;
		memcpy( L, L-32, 16*sizeof(uint8_t) ); 
		memcpy( R, R-32, 16*sizeof(uint8_t) ); 

		for(n=0; n<8; n++)
		{
			for(i=0; i<sizeof(k_buf); i++) k_buf[i] = L[i] ^ p_c_X[i]; // оптимизация: сквозной указатель по итерационным константам.
			p_c_X += 16;
			
			s_Transformation( k_buf );
                
			l_Transformation( k_buf );
                
			// xor с правым подблоком.
			for(i=0; i<sizeof(k_buf); i++) R[i] = k_buf[i] ^ R[i];
        
			swp = R; R = L; L = swp;
		}
	}
}

