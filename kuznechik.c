{
int i;
int n;
uint8_t * p_key;
const uint8_t * msg = in;

	assert( cntx != NULL );

	p_key = cntx->round_key_X[0];
	
	for(n=0;n<9;n++)
	{
		for(i=0; i<16; i++) out[i] = msg[i] ^ p_key[i];
		s_Transformation( out );
		l_Transformation( out );
		p_key += 16;
		if ( 0 == n ) msg = out;
	}

	for(i=0; i<16; i++) out[i] = out[i] ^ p_key[i];	
}

// Дешифрование блока на основе заранее сформированного секретного ключа (контекст).
